/**
 * Generate server (and optional client) loadout authority from shared/avatar-loadout.json.
 *
 * Usage: node scripts/gen-avatar-loadout.mjs
 *        node scripts/gen-avatar-loadout.mjs --check   # exit 1 if generated rust is stale
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const JSON_PATH = path.join(ROOT, 'shared', 'avatar-loadout.json');
const RUST_OUT = path.join(ROOT, 'server', 'spacetimedb', 'src', 'loadout_authority.generated.rs');
const TS_OUT = path.join(ROOT, 'client', 'src', 'avatar', 'loadoutAuthority.generated.ts');

function readAuthority() {
  const raw = fs.readFileSync(JSON_PATH, 'utf8');
  return JSON.parse(raw);
}

function uniqueSorted(values) {
  return [...new Set(values)].sort();
}

function presetGrantList(data, presetId) {
  const preset = data.presets[presetId];
  const grants = [...(preset.extraGrants ?? [])];
  for (const itemId of Object.values(preset.slots ?? {})) {
    const item = data.items[itemId];
    if (item?.grants) grants.push(...item.grants);
  }
  for (const row of preset.utilityEquipment ?? []) {
    const item = data.items[row.itemId];
    if (item?.grants) grants.push(...item.grants);
  }
  for (const g of data.baselineGrants ?? []) grants.push(g);
  return uniqueSorted(grants);
}

function presetEquipmentRows(data, presetId) {
  const preset = data.presets[presetId];
  const rows = [];
  for (const [slot, itemId] of Object.entries(preset.slots ?? {})) {
    rows.push({ slot, itemId });
  }
  for (const row of preset.utilityEquipment ?? []) {
    rows.push({ slot: row.slot, itemId: row.itemId });
  }
  return rows;
}

function rustStringSlice(values) {
  if (values.length === 0) return '&[]';
  return `&[${values.map(v => `"${v}"`).join(', ')}]`;
}

function generateRust(data) {
  const presetIds = Object.keys(data.presets).sort();
  const itemIds = Object.keys(data.items).sort();
  const bodyIds = Object.keys(data.bodies).sort();
  const allGrants = uniqueSorted([
    ...(data.baselineGrants ?? []),
    ...Object.values(data.items).flatMap(i => i.grants ?? []),
    ...Object.values(data.presets).flatMap(p => p.extraGrants ?? []),
  ]);

  const lines = [];
  lines.push('// @generated by scripts/gen-avatar-loadout.mjs — DO NOT EDIT BY HAND');
  lines.push(`// Source: shared/avatar-loadout.json`);
  lines.push('');
  lines.push('#![allow(dead_code)]');
  lines.push('');
  lines.push(`pub const DEFAULT_PRESET_ID: &str = "${data.defaultPresetId}";`);
  lines.push(`pub const BASELINE_GRANTS: &[&str] = ${rustStringSlice(data.baselineGrants ?? [])};`);
  lines.push(`pub const PRESET_IDS: &[&str] = ${rustStringSlice(presetIds)};`);
  lines.push(`pub const BODY_IDS: &[&str] = ${rustStringSlice(bodyIds)};`);
  lines.push(`pub const ITEM_IDS: &[&str] = ${rustStringSlice(itemIds)};`);
  lines.push(`pub const GRANT_IDS: &[&str] = ${rustStringSlice(allGrants)};`);
  lines.push('');

  lines.push('pub fn normalize_preset_id(character_class: &str) -> Result<String, String> {');
  lines.push('    match character_class.trim().to_ascii_lowercase().as_str() {');
  for (const [legacy, preset] of Object.entries(data.legacyClassToPreset ?? {})) {
    lines.push(`        "${legacy}" => Ok("${preset}".to_string()),`);
  }
  lines.push('        _ => Err("Unsupported character class".to_string()),');
  lines.push('    }');
  lines.push('}');
  lines.push('');

  lines.push('pub fn item_grants(item_id: &str) -> &\'static [&\'static str] {');
  lines.push('    match item_id {');
  for (const id of itemIds) {
    const grants = data.items[id].grants ?? [];
    lines.push(`        "${id}" => ${rustStringSlice(grants)},`);
  }
  lines.push('        _ => &[],');
  lines.push('    }');
  lines.push('}');
  lines.push('');

  lines.push('pub fn item_slot(item_id: &str) -> Option<&\'static str> {');
  lines.push('    match item_id {');
  for (const id of itemIds) {
    lines.push(`        "${id}" => Some("${data.items[id].slot}"),`);
  }
  lines.push('        _ => None,');
  lines.push('    }');
  lines.push('}');
  lines.push('');

  const defaultPreset = data.defaultPresetId;
  lines.push('pub fn preset_grants(preset_id: &str) -> &\'static [&\'static str] {');
  lines.push('    match preset_id {');
  for (const id of presetIds) {
    lines.push(`        "${id}" => ${rustStringSlice(presetGrantList(data, id))},`);
  }
  // Non-recursive fallback (same slice as default preset).
  lines.push(`        _ => ${rustStringSlice(presetGrantList(data, defaultPreset))},`);
  lines.push('    }');
  lines.push('}');
  lines.push('');

  lines.push('pub fn preset_body_id(preset_id: &str) -> &\'static str {');
  lines.push('    match preset_id {');
  for (const id of presetIds) {
    lines.push(`        "${id}" => "${data.presets[id].bodyId}",`);
  }
  lines.push(`        _ => "${data.presets[defaultPreset].bodyId}",`);
  lines.push('    }');
  lines.push('}');
  lines.push('');

  lines.push('pub fn preset_scale(preset_id: &str) -> f32 {');
  lines.push('    match preset_id {');
  for (const id of presetIds) {
    lines.push(`        "${id}" => ${Number(data.presets[id].scale).toFixed(1)},`);
  }
  lines.push(`        _ => ${Number(data.presets[defaultPreset].scale).toFixed(1)},`);
  lines.push('    }');
  lines.push('}');
  lines.push('');

  // Equipment as static arrays per preset
  for (const id of presetIds) {
    const rows = presetEquipmentRows(data, id);
    const constName = `PRESET_EQUIPMENT_${id.toUpperCase()}`;
    lines.push(`pub static ${constName}: &[(&str, &str)] = &[`);
    for (const row of rows) {
      lines.push(`    ("${row.slot}", "${row.itemId}"),`);
    }
    lines.push('];');
    lines.push('');
  }

  lines.push('pub fn preset_equipment_pairs(preset_id: &str) -> &\'static [(&\'static str, &\'static str)] {');
  lines.push('    match preset_id {');
  for (const id of presetIds) {
    lines.push(`        "${id}" => PRESET_EQUIPMENT_${id.toUpperCase()},`);
  }
  lines.push(`        _ => PRESET_EQUIPMENT_${defaultPreset.toUpperCase()},`);
  lines.push('    }');
  lines.push('}');
  lines.push('');

  return `${lines.join('\n')}\n`;
}

function generateTs(data) {
  const presetIds = Object.keys(data.presets).sort();
  const authority = {
    baselineGrants: data.baselineGrants ?? [],
    legacyClassToPreset: data.legacyClassToPreset ?? {},
    defaultPresetId: data.defaultPresetId,
    equipSlots: data.equipSlots ?? [],
    bodies: data.bodies,
    items: data.items,
    presets: data.presets,
  };

  // Attach derived lists for parity tests
  const derived = {
    presetIds,
    bodyIds: Object.keys(data.bodies).sort(),
    itemIds: Object.keys(data.items).sort(),
    grantIds: uniqueSorted([
      ...(data.baselineGrants ?? []),
      ...Object.values(data.items).flatMap(i => i.grants ?? []),
      ...Object.values(data.presets).flatMap(p => p.extraGrants ?? []),
    ]),
    presetBodies: Object.fromEntries(presetIds.map(id => [id, data.presets[id].bodyId])),
    presetGrants: Object.fromEntries(presetIds.map(id => [id, presetGrantList(data, id)])),
    presetEquipmentItemIds: Object.fromEntries(
      presetIds.map(id => [
        id,
        uniqueSorted(presetEquipmentRows(data, id).map(r => r.itemId)),
      ]),
    ),
    utilityItemsByPreset: Object.fromEntries(
      presetIds.map(id => [
        id,
        (data.presets[id].utilityEquipment ?? []).map(r => r.itemId),
      ]),
    ),
  };

  return `// @generated by scripts/gen-avatar-loadout.mjs — DO NOT EDIT BY HAND
// Source: shared/avatar-loadout.json

export const LOADOUT_AUTHORITY = ${JSON.stringify(authority, null, 2)} as const;

export const LOADOUT_DERIVED = ${JSON.stringify(derived, null, 2)} as const;
`;
}

function main() {
  const check = process.argv.includes('--check');
  const data = readAuthority();
  const rust = generateRust(data);
  const ts = generateTs(data);

  if (check) {
    const existingRust = fs.existsSync(RUST_OUT) ? fs.readFileSync(RUST_OUT, 'utf8') : '';
    const existingTs = fs.existsSync(TS_OUT) ? fs.readFileSync(TS_OUT, 'utf8') : '';
    if (existingRust !== rust || existingTs !== ts) {
      console.error(
        '[gen-avatar-loadout] generated files are stale. Run: node scripts/gen-avatar-loadout.mjs',
      );
      process.exit(1);
    }
    console.log('[gen-avatar-loadout] generated files up to date');
    return;
  }

  fs.mkdirSync(path.dirname(RUST_OUT), { recursive: true });
  fs.mkdirSync(path.dirname(TS_OUT), { recursive: true });
  fs.writeFileSync(RUST_OUT, rust);
  fs.writeFileSync(TS_OUT, ts);
  console.log(`[gen-avatar-loadout] wrote ${path.relative(ROOT, RUST_OUT)}`);
  console.log(`[gen-avatar-loadout] wrote ${path.relative(ROOT, TS_OUT)}`);
}

main();
